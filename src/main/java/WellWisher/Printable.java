package WellWisher;

/**
 * This is the Printable interface which dictates methods for printing an object's
 * attributes and printing the type of object and description associated to it.
 *
 * @author Quenten Calvano
 * @version 9/17/2021
 */

public interface Printable {

    public abstract void print();//Method to print.
    public abstract void printDescription();//Method to print description and object type.
}
